classdef OptionsView < symphonyui.ui.View

    events
        SelectedNode
        Apply
        Cancel
    end

    properties (Access = private)
        masterList
        detailCardPanel
        generalCard
        applyButton
        cancelButton
    end
    
    properties (Constant)
        GENERAL_CARD    = 1
    end

    methods

        function createUi(obj)
            import symphonyui.ui.util.*;

            set(obj.figureHandle, ...
                'Name', 'Options', ...
                'Position', screenCenter(467, 256));

            mainLayout = uix.VBox( ...
                'Parent', obj.figureHandle, ...
                'Padding', 11, ...
                'Spacing', 7);
            
            optionsLayout = uix.HBox( ...
                'Parent', mainLayout, ...
                'Spacing', 7);
            
            masterLayout = uix.VBox( ...
                'Parent', optionsLayout, ...
                'Padding', 1);

            obj.masterList = uicontrol( ...
                'Parent', masterLayout, ...
                'Style', 'list', ...
                'String', {'General'}, ...
                'Callback', @(h,d)notify(obj, 'SelectedNode'));
            
            detailLayout = uix.VBox( ...
                'Parent', optionsLayout, ...
                'Padding', 1);
            
            obj.detailCardPanel = uix.CardPanel( ...
                'Parent', detailLayout);

            % General card.
            generalGrid = uix.Grid( ...
                'Parent', obj.detailCardPanel, ...
                'Spacing', 7);
            Label( ...
                'Parent', generalGrid, ...
                'String', 'Rig search paths:');
            Label( ...
                'Parent', generalGrid, ...
                'String', 'Protocol search paths:');
            obj.generalCard.rigSearchPathsField = uicontrol( ...
                'Parent', generalGrid, ...
                'Style', 'edit', ...
                'HorizontalAlignment', 'left');
            obj.generalCard.rigSearchPathsField = uicontrol( ...
                'Parent', generalGrid, ...
                'Style', 'edit', ...
                'HorizontalAlignment', 'left');
            set(generalGrid, ...
                'Widths', [120 -1], ...
                'Heights', [25 25]);
            
            set(optionsLayout, 'Widths', [120 -1]);

            % Apply/Cancel controls.
            controlsLayout = uiextras.HBox( ...
                'Parent', mainLayout, ...
                'Spacing', 7);
            uiextras.Empty('Parent', controlsLayout);
            obj.applyButton = uicontrol( ...
                'Parent', controlsLayout, ...
                'Style', 'pushbutton', ...
                'String', 'Apply', ...
                'Interruptible', 'off', ...
                'Callback', @(h,d)notify(obj, 'Apply'));
            obj.cancelButton = uicontrol( ...
                'Parent', controlsLayout, ...
                'Style', 'pushbutton', ...
                'String', 'Cancel', ...
                'Interruptible', 'off', ...
                'Callback', @(h,d)notify(obj, 'Cancel'));
            set(controlsLayout, 'Sizes', [-1 75 75]);

            set(mainLayout, 'Heights', [-1 25]);

            % Set OK button to appear as the default button.
            try %#ok<TRYNC>
                h = handle(obj.figureHandle);
                h.setDefaultButton(obj.applyButton);
            end
        end

        function i = getSelectedNode(obj)
            i = get(obj.masterList, 'Value');
        end

        function setCardSelection(obj, index)
            set(obj.detailCardPanel, 'Selection', index);
        end

    end

end
